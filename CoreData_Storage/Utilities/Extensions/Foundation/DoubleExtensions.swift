import Foundation
import CoreGraphics

// MARK: - Properties
public extension Double {
    
    /// Int.
    public var int: Int {
        return Int(self)
    }
    
    /// Float.
    public var float: Float {
        return Float(self)
    }
    
    /// CGFloat.
    public var cgFloat: CGFloat {
        return CGFloat(self)
    }
    
}
