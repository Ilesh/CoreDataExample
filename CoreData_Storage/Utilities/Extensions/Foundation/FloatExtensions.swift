import Foundation
import CoreGraphics

// MARK: - Properties
public extension Float {
    
    /// Int.
    public var int: Int {
        return Int(self)
    }
    
    /// Double.
    public var double: Double {
        return Double(self)
    }
    
    /// CGFloat.
    public var cgFloat: CGFloat {
        return CGFloat(self)
    }
    
}
